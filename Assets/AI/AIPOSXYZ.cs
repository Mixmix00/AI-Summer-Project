public class AIPOSXYZ{

    private float x;
    private float y;
    private float z;

    public AIPOSXYZ(float X, float Y, float Z){
        x = X;
        y = Y;
        z = Z;
    }

    public float getX(){
        return x;
    }

    public float getY(){
        return y;
    }

    public float getZ(){
        return z;
    }

}
